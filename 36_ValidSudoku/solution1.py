from collections import Counter

def isValidSudoku(board: [[str]]) -> bool:

    for row in board:
        if not legit_list(row):
            return False

    for i in range(9):
        if not legit_list([row[i] for row in board]):
            return False

    for i in range(3):
        for j in range(3):
            x, y = i*3, j*3
            l = []
            for row in range(x, x+3):
                l += board[row][y:y+3]
            if not legit_list(l):
                return False

    return True

def legit_list(l) -> bool:
    d = Counter(l)
    del d["."]
    return all( x == 1 for x in d.values())


